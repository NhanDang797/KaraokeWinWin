     public boolean kiemTraTonTai(String maDV){
        try {
            String sql = "select ma_DV from CTHoaDon where ma_DV = ? and ma_HD = ?";
            PreparedStatement pstm = con.prepareStatement(sql);
            pstm.setString(1, maDV);
            pstm.setString(2, maHoaDon);
            ResultSet rs = pstm.executeQuery();
            if (rs.next()) {
                return true;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return false;
    }
     
