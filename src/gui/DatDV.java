        if (!txtSoluong.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "Số lượng đặt phải là chữ số ");
            txtSoluong.setText("");
            txtSoluong.requestFocus();
            return;
        }
        
        try {
            String sql = "insert into CTHoaDon values(?,?,?,?)";
            PreparedStatement pstm = con.prepareStatement(sql);
            pstm.setString(1, maHoaDon);
            pstm.setString(2, maDichVu);
            pstm.setString(3, txtSoluong.getText());
            pstm.setString(4, "0");
            pstm.executeUpdate();
        } catch (Exception e) {
            e.printStackTrace();
        }
        showDDV();
