    private void showDV() {
        data = new Vector();
        try {
            con = ConnectDB.getConnect();
            String sql = "select ma_DV, ten_DV, gia, sl_ton from Kho where tinh_trang=0";
            PreparedStatement pstm = con.prepareStatement(sql);
            ResultSet rs = pstm.executeQuery();
            while (rs.next()) {
                row = new Vector();
                row.add(rs.getString("ma_DV"));
                row.add(rs.getString("ten_DV"));
                row.add(rs.getString("gia"));
                row.add(rs.getString("sl_ton"));
                data.add(row);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        dvModel = new DefaultTableModel();
        header = new Vector();
        header.add("Mã DV");
        header.add("Tên DV");
        header.add("Đơn giá");
        header.add("Số lượng tồn");

        dvModel.setDataVector(data, header);
        tblDV.setModel(dvModel);

    }

    public void showDDV() {
        data = new Vector();
        try {
            con = ConnectDB.getConnect();
            String sql = "SELECT dbo.CTHoaDon.ma_HD , dbo.Kho.ma_DV, dbo.Kho.ten_DV,dbo.CTHoaDon.so_luong FROM dbo.CTHoaDon, dbo.Kho WHERE dbo.CTHoaDon.ma_DV = dbo.Kho.ma_DV AND dbo.CTHoaDon.tinh_trang = '0' AND dbo.CTHoaDon.ma_HD = ?";
            PreparedStatement pstm = con.prepareStatement(sql);
            pstm.setString(1, maHoaDon);
            ResultSet rs = pstm.executeQuery();
            while (rs.next()) {
                row = new Vector();
                row.add(rs.getString(1));
                row.add(rs.getString(2));
                row.add(rs.getString(3));
                row.add(rs.getString(4));
                data.add(row);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        ddvModel = new DefaultTableModel();
        header = new Vector();
        header.add("Mã Hóa Đơn");
        header.add("Mã Dịch Vụ");
        header.add("Tên Dịch Vụ");
        header.add("Số lượng đặt");

        ddvModel.setDataVector(data, header);
        tblDDV.setModel(ddvModel);

    }

